public class RealationalOp {

	public static void main(String[] args) {
		System.out.println("3 >= 2 : " + (3 >= 2));
		System.out.println("3 <= 2 : " + (3 <= 2));
		System.out.println("7.0 == 7 : " + (7.0 == 7));
		System.out.println("7.0 != 7 : " + (7.0 != 7));
		// 7.0 과 7은 다르지만 자동 형 변환이 일어나서 true를 반환한다
	}

}
